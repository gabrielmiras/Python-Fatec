n=float(input("Digite um número(De 1 a 7 para saber o dia da semana): "))
if n==1:
    print("Segunda-feira")
elif n==2:
    print("Terceira-feira")
elif n==3:
    print("Quarta-feira")
elif n==4:
    print("Quinta-feira")
elif n==5:
    print("Sexta-feira")
elif n==6:
    print("Sábado")
elif n==7:
    print("Domingo")
else:
    print("Número errado , digite um número de 1 A 7")